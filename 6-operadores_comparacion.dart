// OPERADORES DE COMPARACIÓN

// Los operadores de comparación nos permiten comparar dos valores,
// y en Dart son 6, los cuales son: <, >, <=, >=, ==, !=

// Función principal main(no devuelve ningún valor)
void main() {
  // Variables
  int edad1 = 20;
  int edad2 = 24;
  int edad3 = 17;

  print(edad1 < edad3);
  print(edad1 > edad3);
  print(edad1 <= edad3);
  print(edad1 >= edad3);
  print(edad1 == edad3);
  print(edad1 != edad3);
}
