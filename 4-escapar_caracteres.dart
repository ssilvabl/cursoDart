// VARIABLES Y COMENTARIOS

/* Los comentarios de una sola línea se realizan con //
Los multi línea se realizan con /**/
*/

// Iniciar función principal main
void main() {
  // Para escapar caracteres se utiliza el \
  print("Hola, Santiago, tienes \$1000 USDT");

  // Para realizar salto de línea simple se utiliza el \n
  print("¡Hola, mundo!. \n¿cómo estás?");

  // Para el salto de línea mútiple o libre se utilizan las ''' al inicio y al final
  print('''
Hola,
Santiago,
¿cómo estás?
''');
}
